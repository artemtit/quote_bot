def append_quotes(filename: Path, quotes_list: list):
    """Append quotes to file, avoiding duplicates."""
    if filename.exists():
        existing = set(filename.read_text(encoding='utf-8').splitlines())
    else:
        existing = set()
    
    new_lines = [q for q in quotes_list if q not in existing]
    if new_lines:
        mode = 'a' if filename.exists() else 'w'
        with open(filename, mode, encoding='utf-8') as f:
            if filename.exists() and f.tell() > 0:
                f.write('\n')
            f.write('\n'.join(new_lines) + '\n')
        return len(new_lines)
    return 0


