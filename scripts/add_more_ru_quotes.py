def append_more_quotes():
    print("Adding more Russian quotes with authors...\n")
    
    for theme, quotes in ADDITIONAL_RU_QUOTES.items():
        ru_file = OUT_DIR / f'quotes_{theme}.txt'
        if ru_file.exists():
            existing = set(ru_file.read_text(encoding='utf-8').splitlines())
        else:
            existing = set()
        
        new_quotes = [q for q in quotes if q not in existing]
        if new_quotes:
            with open(ru_file, 'a', encoding='utf-8') as f:
                f.write('\n' + '\n'.join(new_quotes) + '\n')
            print(f"✅ {ru_file.name}: +{len(new_quotes)} quotes")
        else:
            print(f"⏭️  {ru_file.name}: all quotes already exist")


if __name__ == '__main__':
    append_more_quotes()
